#!/usr/bin/env python
"""uagent.setup_cli

Interactive setup wizard for end users (installed via pip/wheel).

Purpose:
- Generate a `.env` file in the current working directory.
- Optionally generate shell helper scripts: `env.sh`, `env.ps1`, `env.bat`.

Notes:
- This wizard is intentionally standalone (does not rely on repo `samples/`).
- It only writes to the selected output files in the current directory.
"""

